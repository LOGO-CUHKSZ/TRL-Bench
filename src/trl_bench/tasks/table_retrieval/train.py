#!/usr/bin/env python
"""
Train retrieval projection head on pre-computed embeddings.

This script trains a projection head that maps table embeddings and query embeddings
into a shared retrieval space.

Embedding setup:
- Query embeddings: Always generated by BERT (the text encoder)
- Table embeddings: Any table encoder (BERT, TaBERT, TAPAS, TabSketchFM, etc.)
- Retrieval modes:
  * Hybrid (recommended for non-BERT): Table encoder + BERT table embeddings
    combined via concatenation using create_hybrid_embeddings.py
  * Model-only (baseline): Raw model table embeddings without hybridization.
    Measures whether the model's table embeddings can be directly aligned to
    BERT queries via a learned projection head.
  * Pure BERT: BERT table embeddings + BERT query embeddings (text anchor baseline)

Supports two training modes:
1. Standard mode: Uses in-batch negatives with optional hard negative mining
2. NQT-Retrieval mode: Uses curated data with pre-mined hard negatives

Usage (Hybrid mode — recommended for non-BERT):
    # First create hybrid embeddings:
    python -m downstream_tasks.table_retrieval.create_hybrid_embeddings \\
        --base_variant column_mean \\
        --base_embeddings embeddings/column/tapas/nq_tables.pkl \\
        --bert_embeddings embeddings/column/bert/nq_tables.pkl \\
        --output_path embeddings/column/tapas_bert_hybrid/nq_tables.pkl

    # Then train:
    python -m downstream_tasks.table_retrieval.train \\
        --table_embeddings embeddings/column/tapas_bert_hybrid/nq_tables.pkl \\
        --table_id_mapping datasets/nq_tables/csv/table_id_to_csv.json \\
        --train_query_embeddings embeddings/table_retrieval/bert/queries_train.pkl \\
        --train_questions datasets/nq_tables/json/train.json \\
        --dev_query_embeddings embeddings/table_retrieval/bert/queries_dev.pkl \\
        --dev_questions datasets/nq_tables/json/dev.json \\
        --output_dir assets/checkpoints/table_retrieval/tapas_bert_hybrid

Usage (Model-only — baseline for non-BERT encoders):
    python -m downstream_tasks.table_retrieval.train \\
        --table_embeddings embeddings/column/tapas/nq_tables.pkl \\
        --embedding_variant column_mean \\
        --table_id_mapping datasets/nq_tables/csv/table_id_to_csv.json \\
        --train_query_embeddings embeddings/table_retrieval/bert/queries_train.pkl \\
        --train_questions datasets/nq_tables/json/train.json \\
        --dev_query_embeddings embeddings/table_retrieval/bert/queries_dev.pkl \\
        --dev_questions datasets/nq_tables/json/dev.json \\
        --output_dir assets/checkpoints/table_retrieval/tapas_model_only

Usage (Pure BERT baseline):
    python -m downstream_tasks.table_retrieval.train \\
        --table_embeddings embeddings/column/bert/nq_tables.pkl \\
        --table_id_mapping datasets/nq_tables/csv/table_id_to_csv.json \\
        --train_query_embeddings embeddings/table_retrieval/bert/queries_train.pkl \\
        --train_questions datasets/nq_tables/json/train.json \\
        --dev_query_embeddings embeddings/table_retrieval/bert/queries_dev.pkl \\
        --dev_questions datasets/nq_tables/json/dev.json \\
        --output_dir assets/checkpoints/table_retrieval/bert_bert
"""

