def set_seed(seed: int):
    """Set random seeds for reproducibility."""
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)
        torch.backends.cudnn.deterministic = True
        torch.backends.cudnn.benchmark = False


def get_linear_schedule_with_warmup(optimizer, warmup_steps, total_steps):
    """
    Linear warmup then linear decay scheduler.
    Adapted from NQT-Retrieval/DPR.
    """
    def lr_lambda(current_step):
        if current_step < warmup_steps:
            return float(current_step) / float(max(1, warmup_steps))
        return max(
            1e-7,
            float(total_steps - current_step) / float(max(1, total_steps - warmup_steps))
        )
    return LambdaLR(optimizer, lr_lambda)


def evaluate(
    model: DualProjectionHead,
    table_embeddings: dict,
    query_embeddings: dict,
    questions: list,
    device: str,
    k_values: list = [1, 5, 10, 20, 50, 100],
) -> dict:
    """
    Evaluate retrieval performance with trained projection head.

    Args:
        model: Trained projection head
        table_embeddings: Dict of table_id -> embedding
        query_embeddings: Dict of question_id -> embedding
        questions: List of question dicts with table_id ground truth
        device: Device to run on
        k_values: List of k values for Recall@k

    Returns:
        Dict of evaluation metrics
    """
    model.eval()

    # Project all table embeddings
    table_ids = list(table_embeddings.keys())
    table_emb_array = np.stack([table_embeddings[tid] for tid in table_ids])
    table_emb_tensor = torch.tensor(table_emb_array, dtype=torch.float32, device=device)

    with torch.no_grad():
        # Process in batches to avoid OOM
        batch_size = 1024
        projected_tables = []
        for i in range(0, len(table_emb_tensor), batch_size):
            batch = table_emb_tensor[i:i+batch_size]
            projected = model.forward_table(batch)
            projected_tables.append(projected.cpu().numpy())
        projected_tables = np.concatenate(projected_tables, axis=0)

    # Project all query embeddings
    query_ids = [q['question_id'] for q in questions]
    query_emb_array = np.stack([query_embeddings[qid] for qid in query_ids])
    query_emb_tensor = torch.tensor(query_emb_array, dtype=torch.float32, device=device)

    with torch.no_grad():
        projected_queries = model.forward_query(query_emb_tensor).cpu().numpy()

    # Build index and search
    index = build_index(projected_tables.astype(np.float32), use_gpu=False)
    max_k = max(k_values)
    distances, predictions = search_index(index, projected_queries.astype(np.float32), k=max_k)

    # Build id2table mapping
    id2table = {i: tid for i, tid in enumerate(table_ids)}

    # Get ground truth
    ground_truth = [q['table_id'] for q in questions]

    # Compute metrics
    recall_results = compute_recall_at_k(predictions, ground_truth, id2table, k_values)
    mrr = compute_mrr(predictions, ground_truth, id2table)

    results = {**recall_results, 'MRR': mrr}

    model.train()
    return results


def train(args):
    """Main training function."""
    device = 'cuda' if torch.cuda.is_available() else 'cpu'
    print(f"Using device: {device}")

    # Create output directory
    os.makedirs(args.output_dir, exist_ok=True)

    # Load embeddings
    print("Loading embeddings...")
    tid_mapping = None
    if getattr(args, 'table_id_mapping', None):
        tid_mapping = build_csv_to_table_id_mapping(args.table_id_mapping)
    emb_type = args.embedding_variant
    table_embeddings = load_table_embeddings(args.table_embeddings, emb_type, table_id_mapping=tid_mapping)
    print(f"Loaded {len(table_embeddings)} table embeddings (variant={emb_type})")

    # Determine training mode: curated (NQT-style) or standard
    use_curated_mode = hasattr(args, 'train_curated') and args.train_curated is not None

    if use_curated_mode:
        # NQT-Retrieval style: curated data with pre-mined hard negatives
        print("\n" + "="*60)
        print("TRAINING MODE: NQT-Retrieval (curated with hard negatives)")
        print("="*60)

        # Load curated training data
        curated_data = load_curated_data(args.train_curated)

        # Load query embeddings aligned with curated data
        train_query_embeddings = load_query_embeddings(args.train_query_embeddings)
        print(f"Loaded {len(train_query_embeddings)} train query embeddings")

        # Create curated dataloader
        train_loader = create_curated_dataloader(
            table_embeddings=table_embeddings,
            query_embeddings=train_query_embeddings,
            curated_data=curated_data,
            batch_size=args.batch_size,
            shuffle=True,
            num_hard_negatives=args.num_hard_negatives,
            num_other_negatives=getattr(args, 'num_other_negatives', 0),
            shuffle_positives=getattr(args, 'shuffle_positives', False),
            shuffle_negatives=True,
        )
        train_data = curated_data
        use_hard_neg = True  # Curated mode always uses hard negatives

    else:
        # Standard mode: in-batch negatives with optional hard negative mining
        print("\n" + "="*60)
        print("TRAINING MODE: Standard (in-batch negatives)")
        print("="*60)

        train_query_embeddings = load_query_embeddings(args.train_query_embeddings)
        print(f"Loaded {len(train_query_embeddings)} train query embeddings")

        # Load training data
        train_data = load_training_data(args.train_questions)
        print(f"Train samples: {len(train_data)}")

        # Load hard negatives if provided
        hard_negative_ids = None
        if args.hard_negatives:
            print(f"Loading hard negatives from {args.hard_negatives}...")
            with open(args.hard_negatives, 'r') as f:
                hard_negative_ids = json.load(f)
            print(f"Loaded hard negatives for {len(hard_negative_ids)} queries")

        # Create standard dataloader
        use_hard_neg = args.hard_negatives is not None
        train_loader = create_dataloader(
            table_embeddings=table_embeddings,
            query_embeddings=train_query_embeddings,
            training_data=train_data,
            batch_size=args.batch_size,
            shuffle=True,
            use_hard_negatives=use_hard_neg,
            hard_negative_ids=hard_negative_ids,
            num_hard_negatives=args.num_hard_negatives,
        )

    # Load dev data (always standard format)
    dev_query_embeddings = load_query_embeddings(args.dev_query_embeddings)
    print(f"Loaded {len(dev_query_embeddings)} dev query embeddings")
    dev_data = load_training_data(args.dev_questions)
    print(f"Dev samples: {len(dev_data)}")

    # Auto-detect table embedding dimension from loaded data
    if not table_embeddings:
        raise ValueError("No table embeddings loaded — check embeddings file and table_id_mapping")
    sample_emb = next(iter(table_embeddings.values()))
    actual_table_dim = sample_emb.shape[-1]
    if actual_table_dim != args.table_dim:
        print(f"Auto-detected table_dim={actual_table_dim} (overriding default {args.table_dim})")
        args.table_dim = actual_table_dim

    # Initialize model
    print("Initializing model...")
    if args.init_checkpoint:
        print(f"Loading pretrained checkpoint from {args.init_checkpoint}...")
        model = DualProjectionHead.load(args.init_checkpoint, device)
    else:
        model = DualProjectionHead(
            table_input_dim=args.table_dim,
            query_input_dim=args.query_dim,
            output_dim=args.projection_dim,
            hidden_dim=args.hidden_dim,
            dropout=args.dropout,
            share_projection=args.share_projection,
            # Architecture enhancements
            num_layers=args.num_layers,
            activation=args.activation,
            norm_type=args.norm_type,
            normalize_output=args.normalize_output,
            use_residual=args.use_residual,
            # Adapter layers
            use_adapter=args.use_adapter,
            adapter_bottleneck_dim=args.adapter_bottleneck_dim,
        ).to(device)

    # Initialize optimizer
    optimizer = AdamW(
        model.parameters(),
        lr=args.learning_rate,
        weight_decay=args.weight_decay,
        eps=args.adam_eps,
    )

    # Initialize scheduler
    total_steps = len(train_loader) * args.epochs
    warmup_steps = min(args.warmup_steps, total_steps // 10)
    scheduler = get_linear_schedule_with_warmup(optimizer, warmup_steps, total_steps)

    # Initialize loss
    loss_fn = BiEncoderNllLoss(
        similarity_fn=args.similarity_fn,
        temperature=args.temperature,
        hard_neg_weight=args.hard_neg_weight,
    )

    # Training loop
    print(f"\nStarting training for {args.epochs} epochs...")
    print(f"Total steps: {total_steps}, Warmup steps: {warmup_steps}")
    print(f"Batch size: {args.batch_size}, Learning rate: {args.learning_rate}")
    print(f"Hard negatives: {use_hard_neg} (num={args.num_hard_negatives if use_hard_neg else 0})")
    print(f"Architecture: layers={args.num_layers}, activation={args.activation}, norm={args.norm_type}")
    print(f"Options: normalize_output={args.normalize_output}, use_residual={args.use_residual}")
    print(f"Loss: temperature={args.temperature}, hard_neg_weight={args.hard_neg_weight}")
    if args.use_adapter:
        print(f"Adapter: enabled (bottleneck_dim={args.adapter_bottleneck_dim})")

    best_recall = 0.0
    best_epoch = 0

    for epoch in range(args.epochs):
        model.train()
        epoch_loss = 0.0
        epoch_correct = 0
        epoch_total = 0

        pbar = tqdm(train_loader, desc=f"Epoch {epoch+1}/{args.epochs}")
        for batch_idx, batch in enumerate(pbar):
            # Move to device
            query_embs = batch['query_embs'].to(device)
            context_embs = batch['context_embs'].to(device)
            positive_idx = batch['positive_idx']
            hard_neg_idx = batch.get('hard_neg_idx', None)

            # Forward pass through projection heads
            query_proj = model.forward_query(query_embs)
            context_proj = model.forward_table(context_embs)

            # Compute loss
            loss, correct = loss_fn.calc(query_proj, context_proj, positive_idx,
                                         hard_negative_idx_per_question=hard_neg_idx)

            # Backward pass
            optimizer.zero_grad()
            loss.backward()

            # Gradient clipping
            if args.max_grad_norm > 0:
                torch.nn.utils.clip_grad_norm_(model.parameters(), args.max_grad_norm)

            optimizer.step()
            scheduler.step()

            # Update stats
            epoch_loss += loss.item()
            epoch_correct += correct.item()
            epoch_total += len(positive_idx)

            # Update progress bar
            pbar.set_postfix({
                'loss': f'{loss.item():.4f}',
                'acc': f'{correct.item()/len(positive_idx):.2%}',
                'lr': f'{scheduler.get_last_lr()[0]:.2e}',
            })

        # Epoch stats
        avg_loss = epoch_loss / len(train_loader)
        accuracy = epoch_correct / epoch_total
        print(f"\nEpoch {epoch+1}: Loss={avg_loss:.4f}, Accuracy={accuracy:.2%}")

        # Evaluate on dev set
        if (epoch + 1) % args.eval_every == 0 or epoch == args.epochs - 1:
            print("Evaluating on dev set...")
            dev_results = evaluate(
                model, table_embeddings, dev_query_embeddings, dev_data, device
            )
            print(f"Dev Results: " + ", ".join([f"{k}={v:.4f}" for k, v in dev_results.items()]))

            # Save best model
            recall_100 = dev_results.get('Recall@100', 0)
            if recall_100 > best_recall:
                best_recall = recall_100
                best_epoch = epoch + 1
                best_path = os.path.join(args.output_dir, 'best_model.pt')
                model.save(best_path)
                print(f"New best model saved! Recall@100={recall_100:.4f}")

        # Save checkpoint
        if (epoch + 1) % args.save_every == 0:
            checkpoint_path = os.path.join(args.output_dir, f'checkpoint_epoch{epoch+1}.pt')
            model.save(checkpoint_path)

    # Final summary
    print("\n" + "="*60)
    print("TRAINING COMPLETE")
    print("="*60)
    print(f"Best Recall@100: {best_recall:.4f} (epoch {best_epoch})")
    print(f"Model saved to: {args.output_dir}")
    print("="*60)

    # Save final model
    final_path = os.path.join(args.output_dir, 'final_model.pt')
    model.save(final_path)

    return model


def train_with_refinement(args):
    """
    Train with iterative hard negative refinement.

    Args:
        args: Argument namespace with refinement_rounds parameter

    Refinement rounds:
        - 1: No refinement (original behavior)
        - 2: Optimal - one round of refinement (recommended)
        - N: N-1 refinement rounds after initial training
    """
    if args.refinement_rounds < 1:
        raise ValueError("refinement_rounds must be >= 1")

    # Set random seeds for reproducibility
    set_seed(args.seed)
    print(f"Random seed set to: {args.seed}")

    device = 'cuda' if torch.cuda.is_available() else 'cpu'
    base_output_dir = args.output_dir

    print("=" * 60)
    print(f"Training with {args.refinement_rounds} refinement round(s)")
    print("=" * 60)

    # Round 1: Initial training
    print(f"\n{'='*60}")
    print(f"ROUND 1 / {args.refinement_rounds}: Initial Training")
    print(f"{'='*60}")

    if args.refinement_rounds == 1:
        args.output_dir = base_output_dir
    else:
        args.output_dir = f"{base_output_dir}_r1"

    os.makedirs(args.output_dir, exist_ok=True)
    model = train(args)
    best_model_path = os.path.join(args.output_dir, 'best_model.pt')

    if args.refinement_rounds == 1:
        print("\nRefinement rounds = 1, skipping iterative refinement.")
        return model

    # Import mining function only when needed
    from trl_bench.tasks.table_retrieval.mine_hard_negatives import mine_hard_negatives
    from trl_bench.tasks.table_retrieval.utils.data_utils import (
        load_table_embeddings as _load_table_emb,
        load_query_embeddings as _load_query_emb,
        load_training_data as _load_train_data,
    )

    # Load data for mining (only once)
    print("\nLoading data for hard negative mining...")
    _tid_map = build_csv_to_table_id_mapping(args.table_id_mapping) if getattr(args, 'table_id_mapping', None) else None
    _emb_type = args.embedding_variant
    table_emb_dict = _load_table_emb(args.table_embeddings, _emb_type, table_id_mapping=_tid_map)
    table_ids = sorted(table_emb_dict.keys())
    table_embeddings = np.stack([table_emb_dict[tid] for tid in table_ids])
    id2table = {i: tid for i, tid in enumerate(table_ids)}
    table2id = {tid: i for i, tid in enumerate(table_ids)}
    query_embeddings_dict = _load_query_emb(args.train_query_embeddings)
    questions = _load_train_data(args.train_questions)

    # Store original training settings
    original_lr = args.learning_rate
    original_epochs = args.epochs
    original_warmup = args.warmup_steps

    # Refinement rounds
    for round_num in range(2, args.refinement_rounds + 1):
        print(f"\n{'='*60}")
        print(f"ROUND {round_num} / {args.refinement_rounds}: Iterative Refinement")
        print(f"{'='*60}")

        # Load previous round's best model for mining
        print(f"\nLoading model from {best_model_path} for hard negative mining...")
        projection_head = DualProjectionHead.load(best_model_path, device)

        # Mine hard negatives using projection head
        print(f"\nMining hard negatives with projection head...")
        hard_negatives = mine_hard_negatives(
            table_embeddings=table_embeddings,
            id2table=id2table,
            table2id=table2id,
            query_embeddings_dict=query_embeddings_dict,
            questions=questions,
            top_k=args.mining_top_k,
            num_hard_negatives=args.num_hard_negatives,
            projection_head=projection_head,
            device=device,
        )

        # Save hard negatives
        hn_path = f"{base_output_dir}_r{round_num}_hard_negatives.json"
        with open(hn_path, 'w') as f:
            json.dump(hard_negatives, f)
        print(f"Saved hard negatives to {hn_path}")

        # Update args for refinement training
        args.hard_negatives = hn_path
        args.init_checkpoint = best_model_path
        args.learning_rate = args.refinement_lr
        args.epochs = args.refinement_epochs
        args.warmup_steps = 100  # Shorter warmup for fine-tuning
        args.output_dir = f"{base_output_dir}_r{round_num}"

        os.makedirs(args.output_dir, exist_ok=True)

        print(f"\nFine-tuning with LR={args.learning_rate}, epochs={args.epochs}")
        model = train(args)
        best_model_path = os.path.join(args.output_dir, 'best_model.pt')

    # Copy final best model to base output dir
    os.makedirs(base_output_dir, exist_ok=True)
    final_best_path = os.path.join(base_output_dir, 'best_model.pt')
    shutil.copy(best_model_path, final_best_path)
    print(f"\n{'='*60}")
    print(f"Final best model saved to: {final_best_path}")
    print(f"{'='*60}")

    # Restore original settings (in case args is reused)
    args.learning_rate = original_lr
    args.epochs = original_epochs
    args.warmup_steps = original_warmup
    args.output_dir = base_output_dir

    return model


def main():
    parser = argparse.ArgumentParser(description="Train retrieval projection head")

    # Default paths using TRL conventions
    default_embeddings = str(PROJECT_ROOT / "embeddings/table_retrieval/bert")
    default_dataset = str(PROJECT_ROOT / "datasets/nq_tables")
    default_output = str(PROJECT_ROOT / "assets/checkpoints/table_retrieval/default")

    # Data paths
    parser.add_argument("--table_embeddings", type=str, required=True,
                        help="Path to table embeddings (.pkl) — hybrid or raw model embeddings")
    parser.add_argument("--table_id_mapping", type=str, default=None,
                        help="Path to table_id_to_csv.json for remapping CSV-based table_ids")
    parser.add_argument("--embedding_variant", type=str, default="column_mean",
                        choices=["column_mean", "cls_embedding", "table_embedding", "token_mean"],
                        help="Table embedding variant to extract from pkl files")
    parser.add_argument("--train_query_embeddings", type=str, required=True,
                        help="Path to train query embeddings (.pkl) — always BERT")
    parser.add_argument("--train_questions", type=str, default=None,
                        help="Path to train questions (.json) - for standard mode")
    parser.add_argument("--train_curated", type=str, default=None,
                        help="Path to curated train data (.json) - for NQT-Retrieval mode. "
                             "If provided, uses curated mode with pre-mined hard negatives.")
    parser.add_argument("--dev_query_embeddings", type=str, required=True,
                        help="Path to dev query embeddings (.pkl) — always BERT")
    parser.add_argument("--dev_questions", type=str, required=True,
                        help="Path to dev questions (.json)")
    parser.add_argument("--output_dir", type=str, default=default_output,
                        help="Output directory for checkpoints")

    # Hard negatives
    parser.add_argument("--hard_negatives", type=str, default=None,
                        help="Path to hard negatives file (.json) - for standard mode")
    parser.add_argument("--num_hard_negatives", type=int, default=1,
                        help="Number of hard negatives per query (NQT uses 1)")
    parser.add_argument("--num_other_negatives", type=int, default=0,
                        help="Number of other negatives per query (NQT uses 0)")
    parser.add_argument("--shuffle_positives", action="store_true",
                        help="Shuffle positive contexts during training")

    # Fine-tuning from checkpoint
    parser.add_argument("--init_checkpoint", type=str, default=None,
                        help="Path to pretrained checkpoint to fine-tune from (.pt)")

    # Model architecture
    parser.add_argument("--table_dim", type=int, default=768,
                        help="Table embedding dimension")
    parser.add_argument("--query_dim", type=int, default=768,
                        help="Query embedding dimension")
    parser.add_argument("--projection_dim", type=int, default=256,
                        help="Projection output dimension")
    parser.add_argument("--hidden_dim", type=int, default=256,
                        help="Hidden layer dimension")
    parser.add_argument("--dropout", type=float, default=0.1,
                        help="Dropout rate")
    parser.add_argument("--share_projection", action="store_true",
                        help="Share projection weights between table and query")

    # Architecture enhancements (for experiments)
    parser.add_argument("--num_layers", type=int, default=2,
                        help="Number of layers in projection head (1, 2, 3, ...)")
    parser.add_argument("--activation", type=str, default="relu",
                        choices=["relu"],
                        help="Activation function in projection head")
    parser.add_argument("--norm_type", type=str, default="layer_norm",
                        choices=["none", "layer_norm", "batch_norm"],
                        help="Normalization type in projection head (layer_norm recommended)")
    parser.add_argument("--normalize_output", action="store_true",
                        help="L2 normalize output embeddings")
    parser.add_argument("--use_residual", action="store_true",
                        help="Use residual connections in projection head")

    # Adapter layers (enabled by default based on experimental results showing +7.87% improvement)
    parser.add_argument("--use_adapter", action="store_true", default=True,
                        help="Add bottleneck adapter after projection head (default: enabled)")
    parser.add_argument("--no_adapter", action="store_true",
                        help="Disable adapter layers")
    parser.add_argument("--adapter_bottleneck_dim", type=int, default=64,
                        help="Adapter bottleneck dimension (default: 64)")

    # Training
    parser.add_argument("--seed", type=int, default=42,
                        help="Random seed for reproducibility")
    parser.add_argument("--epochs", type=int, default=60,
                        help="Number of training epochs (60 recommended for convergence)")
    parser.add_argument("--batch_size", type=int, default=512,
                        help="Batch size (512 recommended for in-batch negatives)")
    parser.add_argument("--learning_rate", type=float, default=1.28e-3,
                        help="Learning rate (scales with batch size: lr = 2e-5 * batch_size / 8)")
    parser.add_argument("--weight_decay", type=float, default=0.01,
                        help="Weight decay")
    parser.add_argument("--adam_eps", type=float, default=1e-8,
                        help="Adam epsilon")
    parser.add_argument("--warmup_steps", type=int, default=100,
                        help="Warmup steps")
    parser.add_argument("--max_grad_norm", type=float, default=1.0,
                        help="Max gradient norm for clipping")
    parser.add_argument("--similarity_fn", type=str, default="dot_product",
                        choices=["dot_product", "cosine"],
                        help="Similarity function")

    # Loss function
    parser.add_argument("--temperature", type=float, default=1.0,
                        help="Temperature for softmax scaling (lower = sharper)")
    parser.add_argument("--hard_neg_weight", type=float, default=1.0,
                        help="Weight multiplier for hard negative scores (>1.0 makes them harder)")

    # Evaluation
    parser.add_argument("--eval_every", type=int, default=1,
                        help="Evaluate every N epochs")
    parser.add_argument("--save_every", type=int, default=5,
                        help="Save checkpoint every N epochs")

    # Iterative refinement
    parser.add_argument("--refinement_rounds", type=int, default=2,
                        help="Number of iterative refinement rounds (1=no refinement, 2=optimal, default=2)")
    parser.add_argument("--refinement_lr", type=float, default=5e-6,
                        help="Learning rate for refinement rounds (default: 5e-6)")
    parser.add_argument("--refinement_epochs", type=int, default=20,
                        help="Epochs per refinement round (default: 20)")
    parser.add_argument("--mining_top_k", type=int, default=100,
                        help="Top-k for hard negative mining (default: 100)")

    args = parser.parse_args()

    # Handle --no_adapter flag (overrides default adapter=True)
    if args.no_adapter:
        args.use_adapter = False

    # Validate: either train_questions or train_curated must be provided
    if args.train_questions is None and args.train_curated is None:
        parser.error("Either --train_questions or --train_curated must be provided")

    # If curated mode, disable refinement (hard negatives already pre-mined)
    if args.train_curated is not None and args.refinement_rounds > 1:
        print("Note: Curated mode uses pre-mined hard negatives, disabling iterative refinement")
        args.refinement_rounds = 1

    train_with_refinement(args)


if __name__ == "__main__":
    main()
