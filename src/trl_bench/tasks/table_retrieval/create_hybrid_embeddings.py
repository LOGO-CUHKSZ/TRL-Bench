#!/usr/bin/env python
"""
Create hybrid table embeddings by combining a table encoder's embeddings with
BERT-generated table embeddings.

In this task, query embeddings are always generated by BERT. For non-BERT table
encoders (TAPAS, TaBERT, TabSketchFM, etc.), there is a modality gap between the
query space (BERT) and the table embedding space. Hybrid mode bridges this gap by
combining the table encoder's embeddings with BERT table embeddings via concatenation.
This is the recommended default for all non-BERT table encoders.

Concat is preferred over element-wise addition because it preserves both embedding
spaces as separate inputs for the projection head, and handles dimension mismatches
(e.g., TURL 312-dim + BERT 768-dim). Use --table_dim in train.py to match the
resulting hybrid dimension.

Variant semantics:
  - base_variant: experiment knob — selects which table-level variant to extract
    from the base encoder's pkl (e.g., column_mean, token_mean, cls_embedding).
  - bert_variant: task default — selects which variant to extract from the BERT
    pkl. Defaults to column_mean. Override only for debugging / ablations.

Usage:
    python -m downstream_tasks.table_retrieval.create_hybrid_embeddings \
        --base_variant token_mean \
        --base_embeddings embeddings/column/tapas/nq_tables.pkl \
        --bert_embeddings embeddings/column/bert/nq_tables.pkl \
        --output_path embeddings/column/tapas_bert_hybrid/nq_tables.pkl

    # Override BERT variant (expert use):
    python -m downstream_tasks.table_retrieval.create_hybrid_embeddings \
        --base_variant token_mean --bert_variant cls_embedding \
        --base_embeddings ... --bert_embeddings ... --output_path ...
"""

import argparse
import os
import pickle
import sys
import numpy as np
from pathlib import Path

# Get TRL project root
PROJECT_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from trl_bench.tasks.table_retrieval.utils.data_utils import (
    load_table_embeddings,
    build_csv_to_table_id_mapping,
)


def create_hybrid_embeddings(
    base_embeddings_path: str,
    bert_embeddings_path: str,
    output_path: str,
    base_variant: str,
    bert_variant: str = "column_mean",
    combination_method: str = "concat",
    weight: float = 1.0,
    table_id_mapping_path: str = None,
):
    """
    Create hybrid embeddings by combining two pkl files.

    Args:
        base_embeddings_path: Path to base encoder table embeddings (.pkl)
        bert_embeddings_path: Path to BERT table embeddings (.pkl)
        output_path: Output path for hybrid embeddings (.pkl)
        base_variant: Embedding variant for base model (experiment knob).
        bert_variant: Embedding variant for BERT model (task default, usually
            'column_mean'). Override for debugging or ablations.
        combination_method: How to combine embeddings ('add', 'concat', 'weighted_add')
        weight: Weight for BERT embeddings when using weighted_add (base weight is 1.0)
        table_id_mapping_path: Optional path to table_id_to_csv.json for remapping
            CSV-based table_ids to canonical IDs before intersection.
    """
    tid_mapping = None
    if table_id_mapping_path:
        tid_mapping = build_csv_to_table_id_mapping(table_id_mapping_path)
        print(f"Loaded table_id mapping with {len(tid_mapping)} entries")

    print(f"Loading base embeddings from {base_embeddings_path} (variant={base_variant})")
    base_emb_dict = load_table_embeddings(base_embeddings_path, embedding_type=base_variant,
                                          table_id_mapping=tid_mapping)
    print(f"  Loaded {len(base_emb_dict)} base table embeddings")

    print(f"Loading BERT embeddings from {bert_embeddings_path} (variant={bert_variant})")
    bert_emb_dict = load_table_embeddings(bert_embeddings_path, embedding_type=bert_variant,
                                          table_id_mapping=tid_mapping)
    print(f"  Loaded {len(bert_emb_dict)} BERT table embeddings")

    # Fail fast: if BERT variant yields zero usable embeddings
    if not bert_emb_dict:
        raise ValueError(
            f"BERT embeddings file '{bert_embeddings_path}' yielded zero usable "
            f"embeddings for variant '{bert_variant}'. This variant may not be "
            f"available in the BERT pkl. Try '--bert_variant column_mean'."
        )

    # Find common tables
    common_tables = set(base_emb_dict.keys()) & set(bert_emb_dict.keys())
    print(f"Common tables: {len(common_tables)}")

    if not common_tables:
        print("ERROR: No common tables found between the two embeddings files.")
        sys.exit(1)

    # Create hybrid embeddings for common tables
    hybrid_emb_dict = {}
    for table_id in sorted(common_tables):
        base_emb = base_emb_dict[table_id]
        bert_emb = bert_emb_dict[table_id]

        if combination_method == "add":
            hybrid_emb_dict[table_id] = (base_emb + bert_emb).astype(np.float32)
        elif combination_method == "concat":
            hybrid_emb_dict[table_id] = np.concatenate([base_emb, bert_emb]).astype(np.float32)
        elif combination_method == "weighted_add":
            hybrid_emb_dict[table_id] = (base_emb + weight * bert_emb).astype(np.float32)
        else:
            raise ValueError(f"Unknown combination method: {combination_method}")

    # Save as unified pkl format (list of dicts with table_id and table_embedding)
    # Store hybrid under base_variant key for downstream loader compatibility,
    # and add top-level metadata fields for provenance.
    output_data = []
    for table_id in sorted(hybrid_emb_dict.keys()):
        output_data.append({
            'table_id': table_id,
            'table_embedding': {
                base_variant: hybrid_emb_dict[table_id],
            },
            'hybrid_base_variant': base_variant,
            'hybrid_bert_variant': bert_variant,
            'hybrid_combination_method': combination_method,
        })

    output_dir = os.path.dirname(output_path)
    if output_dir:
        os.makedirs(output_dir, exist_ok=True)
    with open(output_path, 'wb') as f:
        pickle.dump(output_data, f)

    sample_dim = next(iter(hybrid_emb_dict.values())).shape[0]
    print(f"Saved {len(output_data)} hybrid embeddings to {output_path}")
    print(f"Embedding dimension: {sample_dim}")
    print(f"Hybrid provenance: base={base_variant}, bert={bert_variant}, method={combination_method}")

    return hybrid_emb_dict


def main():
    parser = argparse.ArgumentParser(description="Create hybrid table embeddings")

    parser.add_argument("--base_embeddings", type=str, required=True,
                        help="Path to base encoder table embeddings (.pkl)")
    parser.add_argument("--bert_embeddings", type=str, required=True,
                        help="Path to BERT table embeddings (.pkl)")
    parser.add_argument("--output_path", type=str, required=True,
                        help="Output path for hybrid embeddings (.pkl)")
    parser.add_argument("--combination_method", type=str, default="concat",
                        choices=["concat", "add", "weighted_add"],
                        help="How to combine embeddings (default: concat)")
    parser.add_argument("--weight", type=float, default=1.0,
                        help="Weight for BERT embeddings (for weighted_add)")
    parser.add_argument("--base_variant", type=str, required=True,
                        choices=["column_mean", "cls_embedding", "table_embedding", "token_mean"],
                        help="Embedding variant for base model (experiment knob)")
    parser.add_argument("--bert_variant", type=str, default="column_mean",
                        choices=["column_mean", "cls_embedding", "table_embedding", "token_mean"],
                        help="Embedding variant for BERT side (task default: column_mean). "
                             "Override only for debugging or ablations.")
    parser.add_argument("--table_id_mapping", type=str, default=None,
                        help="Path to table_id_to_csv.json for remapping CSV-based table_ids "
                             "to canonical IDs before intersection")

    args = parser.parse_args()

    create_hybrid_embeddings(
        base_embeddings_path=args.base_embeddings,
        bert_embeddings_path=args.bert_embeddings,
        output_path=args.output_path,
        combination_method=args.combination_method,
        weight=args.weight,
        base_variant=args.base_variant,
        bert_variant=args.bert_variant,
        table_id_mapping_path=args.table_id_mapping,
    )


if __name__ == "__main__":
    main()
